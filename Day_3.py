# Simple calculator
q=float(input("Enter first number: "))
w=float(input("Enter second number: "))
print("The value of q+w is {}".format(q+w))
print("The value of q-w is {}".format(q-w))
print("The value of q*w is {}".format(q*w))
print("The value of q/w is {}".format(q/w))
print("The value of q//w is {}".format(q//w))
print("The value of q%w is {}".format(q%w))
print("The value of q**w is {}".format(q**w))